<div class="document-section-header text-center">
    <h3>LISTS OF ACCEPTABLE DOCUMENTS<br> All documents must be UNEXPIRED</h3>
    <p>Employees may present one selection from List A<br>
        or a combination of one selection from List B and one selection from List C.</p>
</div>
<div class="acceptable-docs-list full-width">
    <div class="column-inner">
        <h4 class="text-center"> LIST A <br>
            Documents that Establish
            Both Identity and
            Employment Authorization
        </h4>
        <ul>
            <li>U.S. Passport or U.S. Passport Card</li>
            <li>Permanent Resident Card or Alien
                Registration Receipt Card (Form I-551)</li>
            <li>Foreign passport that contains a
                temporary I-551 stamp or temporary
                I-551 printed notation on a machinereadable
                immigrant visa</li>
            <li>Employment Authorization Document
                that contains a photograph (Form
                I-766)</li>
            <li>For a nonimmigrant alien authorized
                to work for a specific employer
                because of his or her status:
                <div><strong>a.</strong> Foreign passport; and</div>
                <div><strong>b.</strong> Form I-94 or Form I-94A that has
                    the following:</div>
                    <div>(1) The same name as the passport;
                        and</div>
                    <div>(2) An endorsement of the alien's
                        nonimmigrant status as long as
                        that period of endorsement has
                        not yet expired and the
                        proposed employment is not in
                        conflict with any restrictions or
                        limitations identified on the form.</div>


            </li>
            <li>Passport from the Federated States of
                Micronesia (FSM) or the Republic of
                the Marshall Islands (RMI) with Form
                I-94 or Form I-94A indicating
                nonimmigrant admission under the
                Compact of Free Association Between
                the United States and the FSM or RMI</li>
        </ul>
    </div>
    <div class="column-inner text-center or-string">OR</div>
    <div class="column-inner">
        <h4 class="text-center"> LIST B <br>
            Documents that Establish
            Identity
        </h4>
        <ul>
            <li>Driver's license or ID card issued by a
                State or outlying possession of the
                United States provided it contains a
                photograph or information such as
                name, date of birth, gender, height, eye
                color, and address</li>
            <li>ID card issued by federal, state or local
                government agencies or entities,
                provided it contains a photograph or
                information such as name, date of birth,
                gender, height, eye color, and address</li>
            <li>School ID card with a photograph</li>
            <li>Voter's registration card</li>
            <li>U.S. Military card or draft record</li>
            <li>Military dependent's ID card</li>
            <li>U.S. Coast Guard Merchant Mariner
                Card</li>
            <li>Driver's license issued by a Canadian
                government authority</li>
            <li class="text-center no-count-number">
                <strong>For persons under age 18 who are
                    unable to present a document
                    listed above:</strong>
            </li>
            <li>School record or report card</li>
            <li>Clinic, doctor, or hospital record</li>
            <li>Day-care or nursery school record</li>

        </ul>
    </div>
    <div class="column-inner text-center and-string">AND</div>
    <div class="column-inner">
        <h4 class="text-center"> LIST C <br>
            Documents that Establish
            Employment Authorization
        </h4>
        <ul>
            <li>A Social Security Account Number
                card, unless the card includes one of
                the following restrictions:
                <div>(1) NOT VALID FOR EMPLOYMENT</div>
                <div>(2) VALID FOR WORK ONLY WITH
                    INS AUTHORIZATION</div>
                <div>(3) VALID FOR WORK ONLY WITH
                    DHS AUTHORIZATION</div>
            </li>
            <li>Certification of report of birth issued
                by the Department of State (Forms
                DS-1350, FS-545, FS-240)</li>
            <li>Original or certified copy of birth
                certificate issued by a State,
                county, municipal authority, or
                territory of the United States
                bearing an official seal</li>
            <li>Native American tribal document</li>
            <li>U.S. Citizen ID Card (Form I-197)</li>
            <li>Identification Card for Use of
                Resident Citizen in the United
                States (Form I-179)</li>
            <li>Employment authorization
                document issued by the
                Department of Homeland Security</li>

        </ul>
    </div>
</div>